#include "fillit.h"

void	ft_display(char **result)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (result[i])
	{
		while (result[i][j])
		{
			write(1, &result[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		j = 0;
		i++;
	}
}
